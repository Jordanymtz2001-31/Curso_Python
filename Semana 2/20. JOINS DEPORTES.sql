-- INNER JOIN
SELECT *
FROM PERSONA_JOIN P
JOIN DEPORTES D
ON P.DEPORTE_ID = D.ID_DEPORTE; 

-- LEFT JOIN
SELECT P.NOMBRE, P.EDAD, P.OCUPACION, D.DEPORTE
FROM PERSONA_JOIN P
LEFT JOIN DEPORTES D
ON P.DEPORTE_ID = D.ID_DEPORTE;


-- RIGHT JOIN
SELECT * 
FROM PERSONA_JOIN P
RIGHT JOIN DEPORTES D
ON P.DEPORTE_ID = D.ID_DEPORTE;

-- FULL JOIN
SELECT *
FROM PERSONA_JOIN P
LEFT JOIN DEPORTES D
ON P.DEPORTE_ID = D.ID_DEPORTE
UNION
SELECT *
FROM PERSONA_JOIN P
RIGHT JOIN DEPORTES D
ON P.DEPORTE_ID = D.ID_DEPORTE;



                