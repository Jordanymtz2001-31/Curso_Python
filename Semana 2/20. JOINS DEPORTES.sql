CREATE TABLE PERSONA_JOIN(
	ID_PERSONA INT ,
    NOMBRE VARCHAR(25),
    EDAD INT,
    OCUPACION VARCHAR(25),
    DEPORTE_ID INT,
    CONSTRAINT PERSONA_JOINS_PK PRIMARY KEY (ID_PERSONA),
    CONSTRAINT PERSONA_JOINS_FK FOREIGN KEY (DEPORTE_ID) REFERENCES DEPORTES(ID_DEPORTE)
);

CREATE TABLE DEPORTES(
	ID_DEPORTE INT,
    DEPORTE VARCHAR(25),
    CONSTRAINT DEPORTES_PK PRIMARY KEY (ID_DEPORTE)
);

