CREATE TABLE EMPLEADOS_CASE(
	ID_EMPLEADO INT PRIMARY KEY,
    NOMBRE VARCHAR(25),
    APELLIDO VARCHAR(25),
    FECHA_NACIMIENTO DATE,
    FECHA_INGRESO DATE
);

