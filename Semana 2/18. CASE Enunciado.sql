SELECT * FROM EMPLEADOS_CASE;

SELECT CONCAT(UPPER(NOMBRE), " ", LOWER(APELLIDO)) AS NOMBRE_COMPLETO,
LENGTH(CONCAT(NOMBRE, " ", APELLIDO)) AS NUM_CARACTER,
TIMESTAMPDIFF(YEAR, FECHA_NACIMIENTO, CURDATE()) AS EDAD,
TIMESTAMPDIFF(DAY, FECHA_INGRESO, CURDATE()) AS DIAS_TRABAJADOS,
(
	CASE
		WHEN TIMESTAMPDIFF(YEAR, FECHA_NACIMIENTO, CURDATE()) < 30 THEN "ADULTO JOVEN"
		WHEN TIMESTAMPDIFF(YEAR, FECHA_NACIMIENTO, CURDATE()) BETWEEN 30 AND 50 THEN "ADULTO"
        ELSE "ADULTO MAYOR"
        
	END
) AS CLASIFICACION
FROM EMPLEADOS_CASE;

